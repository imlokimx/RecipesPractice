    public void updateTable(){
         DefaultTableModel model = (DefaultTableModel) views.home.home_table.getModel();
        model.setRowCount(0);
        Object rowData[] = new Object[3];
        for(int i = 0; i < recipes.size(); i++)
        {
            rowData[0] = recipes.get(i).getId();
            rowData[1] = recipes.get(i).getName();
            rowData[2] = chefs.get(recipes.get(i).getCid()-1).getName();
            
            model.addRow(rowData);
            home_table.setEnabled(true);
        }
    }
