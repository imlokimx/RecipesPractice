    // Método para agregar recetas a nuestro "almacenamiento"
    public void addRecipe(){
        // Creamos un objeto del tipo recipe
        recipe r1 = new recipe();
        // Tomamos los valores del formulario
        int id = recipes.size()+1;
        String name = r_name_txt.getText();
        String ing = r_ingredients_txt.getText();
        String prep = r_prep_txt.getText();
        int cid = getSelectedChef();
        if(checkEmpty()==0){
             // Asignamos valores al objeto
        r1.setId(id);
        r1.setName(name);
        r1.setIngredients(ing);
        r1.setPreparation(prep);
        r1.setCid(cid);
        // Empujamos el objeto en el almacenamiento (arraylist :p)
        recipes.add(r1);
        updateTable();
        }
    }
    
    public int checkEmpty(){
        int empty = 0;
        String name = views.addRecipe.r_name_txt.getText();
        String ingredients = r_ingredients_txt.getText();
        String preparation = r_prep_txt.getText();
        if("".equals(name)){
            name_msg.setText("Requerido");
            empty = 1;
        }else if(5>name.length()){
        name_msg.setText("Ingrese mas de 5 letras");
        empty = 1;
    } 
        if("".equals(ingredients)){
        ing_msg.setText("Requerido");
            empty = 1;
    }else if(25>ingredients.length()){
        ing_msg.setText("Ingrese al menos 25 caracteres");
            empty = 1;
    }
        if("".equals(preparation)){
        prep_msg.setText("Requerido");
            empty = 1;
    }else if(25>preparation.length()){
        prep_msg.setText("Ingrese al menos 25 caracteres");
            empty = 1;
    }
        return empty;
    }
    
